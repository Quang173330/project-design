        $url = cloudinary()->upload($request->file('avatar')->getRealPath())->getSecurePath();
        
        $res=Restaurant::create([
            'name' => $request->name,
            'address' => $request->address,
            'introduce' => $request->introduce,
            'user_id' => Auth::user()->id
        ]);

        $image= Image::create([
            'url' => $url,
            'imageable_id' => $res->id,
            'imageable_type' => Restaurant::class,
        ]);

